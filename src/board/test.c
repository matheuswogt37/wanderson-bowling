// Função para exibir as instruções do jogo
void exibirInstrucoes() {
    printf("\n===== Instruções do Jogo de Boliche =====\n");
    printf("1. O jogo de boliche consiste em 10 frames.\n");
    printf("2. Em cada frame, você tem duas tentativas para derrubar todos os pinos.\n");
    printf("3. Se derrubar todos os pinos em uma tentativa, isso é um 'strike' e você ganha 10 pontos mais pontos extras.\n");
    printf("4. Se derrubar todos os pinos em duas tentativas, isso é um 'spare' e você ganha 10 pontos mais pontos extras.\n");
    printf("5. Se não derrubar todos os pinos em duas tentativas, você ganha a quantidade de pontos equivalentes aos pinos derrubados.\n");
    printf("6. A pontuação máxima é 300 pontos.\n");
    printf("7. Após cada jogo, sua pontuação máxima será salva e exibida se for um novo recorde.\n");
    printf("=========================================\n\n");
}